# Nhập từ bàn phím 2 số a và b kiểu int, sau đó tính và in ra tổng, hiệu, tích, thương của 2 số đó
a = int(input("Mời các em nhập số a: "))
b = int(input("Mời các em nhập số b:" ))
print("Tổng của a và b là", a + b )
print("Hiệu của a và b là", a - b )
print("Tích của a và b là", a * b )
print("Thương của a và b là", a / b )